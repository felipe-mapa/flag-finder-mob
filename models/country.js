class Country {
    constructor(id, name, flag, capital, continent, population, hdi, year, history, meaning, mainColor, latitude, longitude, tags) {
        this.id = id;
        this.name = name;
        this.flag = flag;
        this.capital = capital;
        this.continent = continent;
        this.population = population;
        this.hdi = hdi;
        this.year = year;
        this.history = history;
        this.meaning = meaning;
        this.mainColor = mainColor;
        this.latitude = latitude;
        this.longitude = longitude;
        this.tags = tags;
    }
}

export default Country